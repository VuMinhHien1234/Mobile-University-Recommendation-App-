package com.example.myapplication.model;

public class ProgramScore {
    public final String name;
    public final String code;
    public final String combo;
    public final String score;

    public ProgramScore(String name, String code, String combo, String score) {
        this.name = name;
        this.code = code;
        this.combo = combo;
        this.score = score;
    }
}
